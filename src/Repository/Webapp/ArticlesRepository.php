class ArticlesRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Articles::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Articles $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Articles $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    public function listbycategory()
    {
        return $this->createQueryBuilder('a')
            ->join('a.category', 'c')
            ->select('
                a.id as id,
                a.name as name,
                a.slug as slug,
                a.content as content,
                a.isShowtitle as isShowtitle,
                a.isShowdate as isShowdate,
                a.state as state,
                a.createdAt as createdAt,
                a.updatedAt as updatedAt,
                c.name as category
            ')
            ->andWhere('c.name = :category')
            ->setParameter('category', 'ActualitÃ©s')
            ->orderBy('a.id', 'ASC')
            ->getQuery()
            ->getResult()
        ;
    }


